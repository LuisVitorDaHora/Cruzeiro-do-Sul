public class numeros_05 {
    public static void main(String[] args) {
        
        System.out.println("Números de 1 a 50 de 1 em 1:");
        for (int i = 1; i <= 50; i++){
        System.out.println( i + " ");
        }
        
        System.out.println("Números de 52 a 100 de 2 em 2:");
        for (int i = 52; i <= 100; i +=2){
        System.out.println( i + "");
        }
    }
}
